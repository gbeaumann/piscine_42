#include <stdio.h>

char	*ft_strlowcase(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 65 && str[i] <= 90)
			str[i] += 32;
		i++;
	}
	return (str);
}

int	main(int argc, char *argv[])
{
	argc = 0;
	printf ("%s\n", ft_strlowcase(argv[1]));
}
