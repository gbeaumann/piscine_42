int	ft_is_prime(int nb)
{
	int	a;
	int	b;

	a = nb;
	b = nb;
	if (nb > 1)
	{
		while (a > 0)
		{	
			b = a;
			while ( b > 0 )
			{
				if (a * b == nb && (b != 1 && a != nb))
					return (0);
				b--;
			}
			a--;
		}
	}
	return (1);
}
